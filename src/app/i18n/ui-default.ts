/* ================= HELPERS ================= */

export function pickLang<T>(
  map: Record<string, T>,
  lang?: string,
  fallback: string = DEFAULT_LANG
): T {
  return map[lang || ''] || map[fallback] || map[DEFAULT_LANG];
}
